package aulas.aula25_64;

public class Aula50_b {
    public static void main(String[] args) {
        //Contagem
        for (int i=0; i<5; i++){
            System.out.println("Valor de i: " + i);
        }

        //Contagem regressiva
        System.out.println("Contagem Regressiva");
        for (int i=4; i>=0; i--){
            System.out.println("Valor de i: " + i);
        }
    }
}
